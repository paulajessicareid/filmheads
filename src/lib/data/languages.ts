export function getLanguageName(code: string): string {
	const name = languageNames.of(code);
	if (!name || name === code) {
		return code;
	}
	return name.charAt(0).toUpperCase() + name.slice(1);
}

function toLanguageOptions(codes: readonly string[]): LanguageOption[] {
	return codes
		.map((code) => ({
			code,
			name: getLanguageName(code)
		}))
		.sort((a, b) => a.name.localeCompare(b.name));
}

export function getCommonLanguageOptions(): LanguageOption[] {
	return toLanguageOptions(COMMON_LANGUAGE_CODES);
}

export function getAllLanguageOptions(): LanguageOption[] {
	return toLanguageOptions(ALL_LANGUAGE_CODES);
}

export function isValidLanguageCode(code: string): boolean {
	return ALL_LANGUAGE_CODES.includes(code as (typeof ALL_LANGUAGE_CODES)[number]);
}
